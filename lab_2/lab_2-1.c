#include <stdio.h>

int main() {
    int a, b; //оголошення двох змінних для зберігання чисел.

    //запит введення першого числа.
    printf("Vvedit pershe chyslo: ");
    scanf("%d", &a); //зчитування першого числа з клавіатури.

    //запит введення другого числа.
    printf("Vvedit druhe chyslo: ");
    scanf("%d", &b); //зчитування другого числа з клавіатури.

    //обмін значень місцями без використання третьої змінної.
    a = a + b; //додавання обох чисел і збереження результату в a.
    b = a - b; //віднімання нового b від суми для отримання початкового значення a.
    a = a - b; //віднімання нового b від суми для отримання початкового значення b.

    //виведення чисел після обміну.
    printf("Pislia obminu:\n"); 
    printf("a = %d\n", a); //виведення нового значення a.
    printf("b = %d\n", b); //виведення нового значення b.

    return 0; //завершення програми з кодом 0.
}
