#include <stdio.h>

int main() {
    char symbol = 'A'; //оголошення змінної типу char та присвоєння їй символу 'A'.

    printf("Symvol: %c\n", symbol); //виведення символу на екран.
    printf("ASCII-kod: %d\n", symbol); //виведення ASCII-коду символу як десяткового числа.

    return 0; //завершення програми з кодом 0.
}
